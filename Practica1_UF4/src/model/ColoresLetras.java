package model;

public class ColoresLetras {
    public static final String ANSI_BLACK = "\u001B[30m";
    public static final String ANSI_RED = "\u001B[31m";
    public static final String ANSI_GREEN = "\u001B[32m";
    public static final String ANSI_YELLOW = "\u001B[33m";
    public static final String ANSI_BLUE = "\u001B[34m";
    public static final String ANSI_PURPLE = "\u001B[35m";
    public static final String ANSI_CYAN = "\u001B[36m";
    public static final String ANSI_WHITE = "\u001B[37m";
    public static final String ANSI_RESET = "\u001B[0m";

    public String getAnsiBlue() {
        return ANSI_BLUE;
    }

    public  String getAnsiReset() {
        return ANSI_RESET;
    }


    public String getANSI_RED() {
        return ANSI_RED;
    }

    public String getANSI_BLACK() {
        return ANSI_BLACK;
    }

    public String getANSI_GREEN() {
        return ANSI_GREEN;
    }

    public String getANSI_YELLOW() {
        return ANSI_YELLOW;
    }

    public String getANSI_PURPLE() {
        return ANSI_PURPLE;
    }

    public String getANSI_CYAN() {
        return ANSI_CYAN;
    }

    public String getANSI_WHITE() {
        return ANSI_WHITE;
    }
}